export class Collectible {
  constructor({x, y, value, id}) {
    this.x = x;
    this.y = y;
    this.value = value;
    this.id = id;
    this.rad = 15;
  }

  draw(ctx, img) {
    ctx.drawImage(img, this.x, this.y, this.rad, this.rad);
  }
}

/*
  Note: Attempt to export this for use
  in server.js
*/
try {
  module.exports = Collectible;
} catch(e) {}

export default Collectible;
